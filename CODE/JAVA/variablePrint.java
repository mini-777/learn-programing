public class variablePrint {// public 클래스 생성, 클래스명 HelloJava
	    public static void main(String[] args) {
        int a = 0206; //정수는 int 로 선언
        long b = 1500000L; //long 타입은 long으로 선언
        char c ='A'; //문자 선언은 char
        String d ="컴퓨터"; // 문자열 선언은 String *대문자 사용에 주의
        String e ="내가 작성한 \"자바\" 프로그램"; // 큰 따옴표를 작성하기 위해 \"를 사용
        double f = 3.141592653589793; //float 범위를 초과해 double 사용
        float g = 3500; //실수는 float로 선언

        
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g); //문자열 함수 println을 사용해서 출력
        
    }
 
}